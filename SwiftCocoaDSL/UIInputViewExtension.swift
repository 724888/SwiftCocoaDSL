extension UIInputView {
    @discardableResult
  func inputViewStyle(_ inputViewStyle: UIInputViewStyle) -> Self {
self.inputViewStyle = inputViewStyle
return self
}
    @discardableResult
  func allowsSelfSizing(_ allowsSelfSizing: BOOL) -> Self {
self.allowsSelfSizing = allowsSelfSizing
return self
}

  @discardableResult
  func initWithFrame(initWithFrame frame: CGRect, inputViewStyle inputViewStyle: UIInputViewStyle) -> Self {
return self
}

  @discardableResult
  func initWithCoder(initWithCoder aDecoder: NSCoder) -> Self {
return self
}
}
