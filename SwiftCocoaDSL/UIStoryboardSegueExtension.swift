extension UIStoryboardSegue {

  @discardableResult
  func initWithIdentifier(initWithIdentifier identifier: NSString, source source: UIViewController, destination destination: UIViewController) -> Self {
return self
}

  @discardableResult
  func ()->Self {
return self
}
    @discardableResult
  func identifier(_ identifier: NSString) -> Self {
self.identifier = identifier
return self
}
    @discardableResult
  func UIViewController(_ UIViewController: __kindof) -> Self {
self.UIViewController = UIViewController
return self
}
    @discardableResult
  func UIViewController(_ UIViewController: __kindof) -> Self {
self.UIViewController = UIViewController
return self
}

  @discardableResult
  func ()->Self {
return self
}

  @discardableResult
  func ()->Self {
return self
}
    @discardableResult
  func sourceViewController(_ sourceViewController: UIViewController) -> Self {
self.sourceViewController = sourceViewController
return self
}
    @discardableResult
  func unwindAction(_ unwindAction: SEL) -> Self {
self.unwindAction = unwindAction
return self
}
    @discardableResult
  func sender(_ sender: id) -> Self {
self.sender = sender
return self
}
}
