extension UIImageView {

  @discardableResult
  func initWithImage(initWithImage image: UIImage) -> Self {
return self
}

  @discardableResult
  func initWithImage(initWithImage image: UIImage, highlightedImage highlightedImage: UIImage, NS_AVAILABLE_IOS UIImage: strong) -> Self {
return self
}
    @discardableResult
  func highlightedImage(_ highlightedImage: UIImage) -> Self {
self.highlightedImage = highlightedImage
return self
}
    @discardableResult
  func animationDuration(_ animationDuration: NSTimeInterval) -> Self {
self.animationDuration = animationDuration
return self
}
    @discardableResult
  func animationRepeatCount(_ animationRepeatCount: NSInteger) -> Self {
self.animationRepeatCount = animationRepeatCount
return self
}
    @discardableResult
  func tintColor(_ tintColor: UIColor) -> Self {
self.tintColor = tintColor
return self
}

  @discardableResult
  func ()->Self {
return self
}

  @discardableResult
  func ()->Self {
return self
}
    @discardableResult
  func adjustsImageWhenAncestorFocused(_ adjustsImageWhenAncestorFocused: BOOL) -> Self {
self.adjustsImageWhenAncestorFocused = adjustsImageWhenAncestorFocused
return self
}
    @discardableResult
  func focusedFrameGuide(_ focusedFrameGuide: UILayoutGuide) -> Self {
self.focusedFrameGuide = focusedFrameGuide
return self
}
    @discardableResult
  func overlayContentView(_ overlayContentView: UIView) -> Self {
self.overlayContentView = overlayContentView
return self
}
    @discardableResult
  func masksFocusEffectToContents(_ masksFocusEffectToContents: BOOL) -> Self {
self.masksFocusEffectToContents = masksFocusEffectToContents
return self
}
}
