  func representativeItem(_ representativeItem: UIBarButtonItem) -> Self {
self.representativeItem = representativeItem
return self
}
    @discardableResult
  func buttonGroup(_ buttonGroup: UIBarButtonItemGroup) -> Self {
self.buttonGroup = buttonGroup
return self
}
}
